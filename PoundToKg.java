public class PoundToKg {
    public static void main(String[] args) {
        int noOfPounds = 5;

        double Kg = noOfPounds * 0.45359237 ;
        System.out.println(noOfPounds +" Pounds in Kg : "+Kg);
        int Kgi = (int) (noOfPounds * 0.45359237) ;
        System.out.println(noOfPounds +" Pounds in Kg in Int value is : "+Kgi);
    }
}
